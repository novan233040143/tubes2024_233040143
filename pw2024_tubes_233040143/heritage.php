        <h2>Gedung Merdeka</h2>
        <h4>
          Banyak sekali peristiwa penting yang terjadi di Bandung pada saat
          Indonesia masih berusaha merdeka. Salah satu saksi bisu perjuangan
          tersebut ialah Gedung Merdeka. Gedung ini menjadi tempat pelaksanaan
          peristiwa bersejarah di tahun 1955, yakni Konferensi Asia-Afrika.
          <br />
          <br />
          Arsitek dari Gedung Merdeka ialah Van Galen Last dan C.P. Wolff
          Schoemaker, dua orang guru besar Sekolah Teknik Tinggi (sekarang ITB)
          pada masa penjajahan Jepang. Gedung Merdeka sendiri memiliki luas 7500
          m2. Gedung ini memiliki nuansa art deco dengan sentuhan megah lewat
          marmer buatan Italia, tak ketinggalan pencahayaan mewah melalui
          lampu-lampu kristal yang menggantung.
          <br />
          <br />
          Alamat: Jl. Asia Afrika No.65, Braga, Kec. Sumur Bandung, Kota
          Bandung, Jawa Barat 40111 Jam Buka: Senin - Jumat (08.00 - 16.00)
          Tiket Masuk: Gratis
        </h4>
