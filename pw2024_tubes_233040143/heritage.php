        <h2>Monumen Bandung Lautan Api</h2>
        <h4>
          Monumen Bandung Lautan Api dibangun untuk mengenang peristiwa
          perlawanan masyarakat Bandung untuk mempertahankan wilayahnya dari
          kedatangan tentara sekutu dan NICA. Pada saat itu, rakyat diminta oleh
          Belanda untuk mengosongkan wilayah Bandung. Namun, rakyat bersikeras
          mempertahankan wilayah Bandung dan berakhir dengan membakar seluruh
          rumah agar tidak bisa diambil alih oleh para sekutu. Peristiwa
          perlawanan tersebut kemudian dikenal sebagai peristiwa Bandung Lautan
          Api.
          <br />
          <br />
          Monumen ini memiliki desain yang sangat menggambarkan peristiwa
          membumihanguskan Bandung pada masa itu. Berdesain patung dengan bentuk
          kobaran api menyala pada ujungnya dan memiliki tiga penyangga untuk
          menambah kegagahan dari bentuk Monumen Bandung Lautan Api.
          Disekelilingnya terdapat kolam dan pagar agar Monumen Bandung Lautan
          Api bisa tetap terawat dan tidak dirusak fasilitasnya. Kamu bisa
          datang ke Jalan BKR Ciateul untuk melihat secara langsung penampakan
          dari Monumen Bandung Lautan Api.
        </h4>
