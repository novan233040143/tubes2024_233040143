        <h2>Museum Konferensi Asia Afrika</h2>
        <h4>
          Nah, sebelumnya sudah membahas mengenai Gedung Merdeka, di dalam
          gedung tersebut terdapat salah satu museum yang boleh kamu jadikan
          destinasi wisata sejarah. Konferensi Asia Afrika merupakan momen
          penting dalam dunia politik di Indonesia sekaligus menampakkan diri di
          depan negara Asia-Afrika lainnya.
          <br />
          <br />
          Di Museum Konferensi Asia Afrika, kamu dapat melihat kilas balik momen
          bersejarah Indonesia dalam mengadakan konferensi Internasional.
          Konferensi Asia Afrika ini telah melahirkan Dasasila Bandung sebagai
          pedoman negara-negara jajahan memperjuangkan kemerdekaannya. Museum
          ini memiliki fasilitas berupa ruang pameran permanen yang menampilkan
          dokumentasi KAA tahun 1955, ruang audio visual, riset dan aktivitas,
          serta ruang perpustakaan.
          <br />
          <br />
          Alamat: Jl. Asia Afrika No.65, Braga, Kec. Sumur Bandung, Kota
          Bandung, Jawa Barat 40111 Jam Buka: Selasa, Kamis, Sabtu, dan Minggu
          (09.00 - 16.00) Tiket Masuk: Gratis
        </h4>
