        <h2>Bandros (Bandung Tour On Bus)</h2>
        <h4>
          Bagi para wisatawan yang ingin berkeliling Bandung tanpa harus pusing
          memikirkan rute, Bandros bisa jadi pilihan yang tepat. Bandros
          merupakan singkatan dari Bandung Tour on Bus. Bus ini difungsikan
          untuk mengatarkan wisatawan ke tempat wisata menarik lengkap dengan
          tour guide yang akan menjelaskan setiap tempat wisata yang dilewati. <br> <br>
          Karena memiliki rute yang beragam, Bandros menandai setiap rute
          melalui warna bus. Seperti Bandros biru memiliki rute alun-alun hingga
          kawasan Buahbatu, Bandros kuning dari lapangan Gasibu hingga Braga,
          Bandros ungu dari lapangan Gasibu hingga Saung Angklung Udjo, dan
          lainnya. Dengan tarif Rp 20.000, detikers bisa berkeliling Kota
          Bandung sekaligus menambah wawasan tentang tempat yang ada di Kota
          Bandung. Bandros hadir setiap hari pukul 08.00 - 16.00 WIB.
